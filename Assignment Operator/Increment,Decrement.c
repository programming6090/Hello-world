#include <stdio.h>

int main(void)
{
    int val = 0;

    printf("the value of val: %d\n", val);
    printf("the value of val: %d\n", val++);
    printf("the value of val: %d\n", val);
    printf("the value of val: %d\n", ++val);
    printf("the value of val: %d\n", val);
    printf("the value of val: %d\n", val--);
    printf("the value of val: %d\n", val);
    printf("the value of val: %d\n", --val);
    printf("the value of val: %d\n", val);





    return 0;
}
